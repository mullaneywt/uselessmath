"""Arithmatic functions for useless math"""


def uadd(x: int, y: int) -> int:
    """Uselessly add two numbers"""
    return x + y


def usub(x: int, y: int) -> int:
    """Uselessly subtract two numbers"""
    return x - y


def umult(x: int, y: int) -> int:
    """Uselessly multiply two numbers"""
    return x * y


def udiv(x: int, y: int) -> int:
    """Uselessly divide two numbers"""
    return x / y


def umod(x: int, y: int) -> int:
    """Uselessly divide two numbers with modular division"""
    return x % y
