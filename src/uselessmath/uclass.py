"""Class for performing useless math"""

from uselessmath import ucube, usquare


class Umath:
    """This class brings new levels of overcomplication, previously unheard of"""

    def add(self, x: int, y: int) -> int:
        """Uselessly add two numbers"""
        return x + y

    def sub(self, x: int, y: int) -> int:
        """Uselessly subtract two numbers"""
        return x - y

    def mult(self, x: int, y: int) -> int:
        """Uselessly multiply two numbers"""
        return x * y

    def div(self, x: int, y: int) -> int:
        """Uselessly divide two numbers"""
        return x / y

    def square(self, x: int) -> int:
        """Uselessly square a number"""
        return usquare(x)

    def cube(self, x: int) -> int:
        """Uselessly cube a number"""
        return ucube(x)
